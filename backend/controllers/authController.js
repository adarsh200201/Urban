// @desc    Register user
// @route   POST /api/auth/register
// @access  Public
exports.register = async (req, res) => {
  try {
    console.log('Register endpoint hit with data:', { ...req.body, password: '******' });
    
    const { name, email, password, phone } = req.body;

    // Check if user exists
    let user;
    try {
      user = await User.findOne({ email });
      console.log('User lookup result:', user ? 'User exists' : 'User does not exist');
    } catch (dbError) {
      console.error('Database error during user lookup:', dbError);
      return res.status(500).json({
        success: false,
        message: 'Database connection error',
        error: dbError.message
      });
    }
    
    if (user) {
      return res.status(400).json({
        success: false,
        message: 'User with this email already exists'
      });
    }

    // Create new user
    try {
      user = await User.create({
        name,
        email,
        password,
        phone
      });
      console.log('User created successfully with ID:', user._id);
    } catch (createError) {
      console.error('Error creating user:', createError);
      return res.status(500).json({
        success: false,
        message: 'Failed to create user',
        error: createError.message,
        details: createError.errors || {}
      });
    }

    sendTokenResponse(user, 201, res);
  } catch (error) {
    console.error('Unhandled error in registration:', error);
    res.status(500).json({
      success: false,
      message: 'Server error',
      error: error.message,
      stack: process.env.NODE_ENV === 'development' ? error.stack : undefined
    });
  }
};

// @desc    Login user
// @route   POST /api/auth/login
// @access  Public
exports.login = async (req, res) => {
  try {
    const { email, password } = req.body;

    // Validate email & password
    if (!email || !password) {
      return res.status(400).json({
        success: false,
        message: 'Please provide email and password'
      });
    }

    // Check for user
    const user = await User.findOne({ email }).select('+password');
    if (!user) {
      return res.status(401).json({
        success: false,
        message: 'Invalid credentials'
      });
    }

    // Check if password matches
    const isMatch = await user.matchPassword(password);
    if (!isMatch) {
      return res.status(401).json({
        success: false,
        message: 'Invalid credentials'
      });
    }

    sendTokenResponse(user, 200, res);
  } catch (error) {
    res.status(500).json({
      success: false,
      message: 'Server error',
      error: error.message
    });
  }
};

